    private void SetImpacts(LogImpact[] impacts)
    {
        LogImpactContainer.RemoveAllChildren();
        _impactButtons.Clear();

        foreach (var impact in impacts)
        {
            var button = new Button
            {
                Text = impact.ToString(),
                ToggleMode = true,
                Pressed = true,
                MinHeight = 26,
            };
            button.AddStyleClass("ButtonSmall");

            // Apply tinted flat style
            if (ImpactColors.TryGetValue(impact, out var colors))
            {
                button.StyleBoxOverride = new StyleBoxFlat
                {
                    BackgroundColor = colors.Bg,
                    ContentMarginLeftOverride = 6,
                    ContentMarginRightOverride = 6,
                    ContentMarginTopOverride = 2,
                    ContentMarginBottomOverride = 2,
                };
                button.Label.FontColorOverride = colors.Text;
            }

            SelectedImpacts.Add(impact);

            // Capture for closure
            var capturedImpact = impact;
            button.OnPressed += args =>
            {
                if (args.Button.Pressed)
                    SelectedImpacts.Add(capturedImpact);
                else
                    SelectedImpacts.Remove(capturedImpact);

                UpdateLogs();
            };

            _impactButtons[impact] = button;
            LogImpactContainer.AddChild(button);
        }
    }

