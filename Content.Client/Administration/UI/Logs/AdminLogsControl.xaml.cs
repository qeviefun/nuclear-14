    /// <summary>
    /// Updates the accent bar, header background, and type button styles for ALL categories.
    /// Called after any selection change so the rail always reflects current state.
    /// </summary>
    private void UpdateCategoryAccents()
    {
        foreach (var (category, entry) in _categoryEntries)
        {
            var hasSelected = entry.Types.Any(t => SelectedTypes.Contains(t));
            var accentColor = hasSelected && CategoryAccentColors.TryGetValue(category, out var colors)
                ? colors.Accent
                : Color.FromHex("#252540");

            // Accent bar — bright category color when any child is selected
            entry.AccentBar.PanelOverride = new StyleBoxFlat { BackgroundColor = accentColor };

            // Header button — tinted when expanded, default when collapsed
            UpdateCategoryHeaderStyle(category, entry);

            // Type buttons — tinted background when selected, dim when unselected
            UpdateTypeButtonStyles(category, entry);
        }
    }

    /// <summary>
    /// Updates a single category header button background.
    /// Expanded = category-tinted background so it pops.
    /// Collapsed = default dark background.
    /// </summary>
    private void UpdateCategoryHeaderStyle(string category, CategoryEntry entry)
    {
        Color bg;
        Color textColor;

        if (entry.Expanded && CategoryAccentColors.TryGetValue(category, out var colors))
        {
            // Expanded — blend category tint into the base for a visible highlight
            var baseBg = Color.FromHex("#181830");
            bg = new Color(
                baseBg.R * 0.5f + colors.DimBg.R * 0.5f,
                baseBg.G * 0.5f + colors.DimBg.G * 0.5f,
                baseBg.B * 0.5f + colors.DimBg.B * 0.5f,
                1f);
            textColor = colors.Accent;
        }
        else
        {
            bg = Color.FromHex("#181830");
            textColor = Color.FromHex("#ccccdd");
        }

        entry.HeaderButton.StyleBoxOverride = new StyleBoxFlat
        {
            BackgroundColor = bg,
            ContentMarginLeftOverride = 4,
            ContentMarginRightOverride = 4,
            ContentMarginTopOverride = 2,
            ContentMarginBottomOverride = 2,
        };
        entry.HeaderButton.Label.FontColorOverride = textColor;
    }

    /// <summary>
    /// Re-styles every LogType toggle button in a category.
    /// Selected = tinted with category color.
    /// Unselected = dim dark background with muted text.
    /// </summary>
    private void UpdateTypeButtonStyles(string category, CategoryEntry entry)
    {
        CategoryAccentColors.TryGetValue(category, out var colors);
        var selectedBg = colors.DimBg;
        var selectedText = colors.Accent;
        var unselectedBg = Color.FromHex("#0c0c16");
        var unselectedText = Color.FromHex("#555566");

        foreach (var child in entry.TypesContainer.Children)
        {
            if (child is not AdminLogTypeButton typeBtn)
                continue;

            var isSelected = SelectedTypes.Contains(typeBtn.Type);
            typeBtn.StyleBoxOverride = new StyleBoxFlat
            {
                BackgroundColor = isSelected ? selectedBg : unselectedBg,
                ContentMarginLeftOverride = 4,
                ContentMarginRightOverride = 4,
                ContentMarginTopOverride = 1,
                ContentMarginBottomOverride = 1,
            };
            typeBtn.Label.FontColorOverride = isSelected ? selectedText : unselectedText;
        }
    }

    /// <summary>
    /// Updates the count labels on each category header with live counts
    /// of currently visible logs in that category.
    /// </summary>
    private void UpdateCategoryCounts()
    {
        // Count visible logs per category
        var counts = new Dictionary<string, int>();
        foreach (var child in LogsContainer.Children)
        {
            if (child is not AdminLogRow row || !child.Visible)
                continue;

            var cat = row.Category;
            counts[cat] = counts.GetValueOrDefault(cat) + 1;
        }

        foreach (var (category, entry) in _categoryEntries)
        {
            var count = counts.GetValueOrDefault(category);
            entry.CountLabel.Text = count > 0 ? $"({count})" : "";
        }
    }

