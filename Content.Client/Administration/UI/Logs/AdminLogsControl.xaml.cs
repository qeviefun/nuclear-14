    // #Misfits Add — popup for rarely-used advanced filter controls.
    // Built lazily on first click, reused afterward.
    private void ToggleFilterPopup(ButtonEventArgs args)
    {
        if (_filterPopup != null && _filterPopup.Visible)
        {
            _filterPopup.Visible = false;
            return;
        }

        if (_filterPopup == null)
        {
            _filterPopup = new Popup();
            var panel = new PanelContainer
            {
                PanelOverride = new StyleBoxFlat
                {
                    BackgroundColor = Color.FromHex("#181830"),
                    ContentMarginLeftOverride = 12,
                    ContentMarginRightOverride = 12,
                    ContentMarginTopOverride = 8,
                    ContentMarginBottomOverride = 8,
                    BorderThickness = new Thickness(1),
                    BorderColor = Color.FromHex("#252540"),
                },
            };

            var content = new BoxContainer
            {
                Orientation = BoxContainer.LayoutOrientation.Vertical,
                SeparationOverride = 6,
                MinWidth = 200,
            };

            var header = new Label
            {
                Text = "Advanced Filters",
                FontColorOverride = Color.FromHex("#ffffff"),
            };
            header.AddStyleClass("LabelHeading");
            content.AddChild(header);

            // Info label
            var info = new Label
            {
                Text = "Use the category rail on the left to\nfilter by log type. Use severity pills\nabove to filter by impact level.",
                FontColorOverride = Color.FromHex("#8888aa"),
            };
            info.AddStyleClass("LabelSmall");
            content.AddChild(info);

            panel.AddChild(content);
            _filterPopup.AddChild(panel);

            UserInterfaceManager.ModalRoot.AddChild(_filterPopup);
        }

        // Position popup below the filters button
        var globalPos = FiltersButton.GlobalPosition;
        _filterPopup.Open(UIBox2.FromDimensions(globalPos + new Vector2(0, FiltersButton.Height), new Vector2(220, 130)));
    }

