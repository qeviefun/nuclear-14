using System.Collections.Generic;
using System.Diagnostics.CodeAnalysis;
using Content.Client._Misfits.Guidebook;
using JetBrains.Annotations;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;

namespace Content.Client._Misfits.Guidebook.Controls;

[UsedImplicitly, GenerateTypedNameReferences]
public sealed partial class GuideCodeBlock : GuideTagContainer
{
    public GuideCodeBlock()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);

        Margin = new Thickness(0, 0, 0, GuidebookTheme.ComponentBottomMargin);
        Background.PanelOverride = GuidebookTheme.CreatePanel(GuidebookTheme.CodeBackground, GuidebookTheme.CodeBorder);
        GuidebookTheme.ApplySubSectionTitle(TitleLabel);
        TitleLabel.FontColorOverride = GuidebookTheme.CodeBorder;

        ConfigureContentHost(ContentBody);
    }

    public override bool TryParseTag(Dictionary<string, string> args, [NotNullWhen(true)] out Control? control)
    {
        if (args.TryGetValue("Title", out var title) && !string.IsNullOrWhiteSpace(title))
        {
            TitleLabel.Visible = true;
            TitleLabel.Text = title;
        }
        else
        {
            TitleLabel.Visible = true;
            TitleLabel.Text = "Snippet";
        }

        control = this;
        return true;
    }
}